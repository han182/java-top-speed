package chapter02;

public class Java01_Variable {
    public static void main(String[] args) {
        // TODO 变量，其实就是可以改变的向量存储
        // 变量声明: 数据类型 变量名称
        String name;
        // 变量赋值
        name = "hs";
        // 变量使用
        System.out.println(name);
        // 声明和赋值可以写在一起
        String name2 = "hs1";
        System.out.println(name2);

        //在使用变量之前，必须给变量赋值。这个操作叫变量的初始化
    }
}
